package com.ict.data.vo;

public class ProjectDatabaseVO {
	private String product_idx, product_name, sellers_idx, description, price, stock, 
	category_idx, created_at, is_active, thumbnail_url;

	public String getProduct_idx() {
		return product_idx;
	}

	public void setProduct_idx(String product_idx) {
		this.product_idx = product_idx;
	}

	public String getProduct_name() {
		return product_name;
	}

	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getStock() {
		return stock;
	}

	public void setStock(String stock) {
		this.stock = stock;
	}

	public String getCategory_idx() {
		return category_idx;
	}

	public void setCategory_idx(String category_idx) {
		this.category_idx = category_idx;
	}

	public String getCreated_at() {
		return created_at;
	}

	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}

	public String getIs_active() {
		return is_active;
	}

	public void setIs_active(String is_active) {
		this.is_active = is_active;
	}

	public String getThumbnail_url() {
		return thumbnail_url;
	}

	public void setThumbnail_url(String thumbnail_url) {
		this.thumbnail_url = thumbnail_url;
	}

	public String getSellers_idx() {
		return sellers_idx;
	}

	public void setSellers_idx(String sellers_idx) {
		this.sellers_idx = sellers_idx;
	}
	
}
